    public void SalvarAluno() {
        if (VerifcarInPuts() == true) {

            int idturma = new TurmaContr().PegaIdTurma_aPArtirNome(jComboBoxTurmaAluno.getSelectedItem().toString());
            txtIdeTurmaAluno.setText(String.valueOf(idturma));
            int classe = Integer.parseInt(jComboClasseAluno.getSelectedItem().toString());
            String turma = jComboBoxTurmaAluno.getSelectedItem().toString();
            String curso = jComboBoxCursoAluno.getSelectedItem().toString();
            String nome = txtNomeAluno.getText();
            String genero = jComboBoxGeneroAluno.getSelectedItem().toString();
            int numAluno = Integer.parseInt(txtNOrdemAluno.getText());
            int processo = Integer.parseInt(txtNumProcAluno.getText());

            new AlunoContr().Salvar(idturma, genero, numAluno, processo, nome, classe, turma, curso);
            JOptionPane.showMessageDialog(this, "Salvo com Sucesso");
            this.LimparAluno();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione: 1-)Curso 2-)Genero 3-)Turma \n Preencha Todos os Campos...");
            this.LimparAluno();
        }

    }

